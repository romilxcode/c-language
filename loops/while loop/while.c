// while loop:

// A while loop repeatedly executes a block of code as long as a specified condition is true.

// Syntax:

// while (condition) {  
//     // code to be executed
// }

#include<stdio.h>

int main(){
    int i=1;
    while(i<=5) {
        printf("Hello world\n");
        i++;
    }
    return 0;
}