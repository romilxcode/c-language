// pointers in function call
// call by value: we pass the value of variable to function.
// call by reference: we pass the address of variable to function.
// in call by reference we use pointer to pointer.
// in call by reference we can change the value of variable in function.
// in call by value we cannot change the value of variable in function.
// in call by reference we can save memory.
// in call by value we cannot save memory.
// in call by reference we can return multiple values from function.
// in call by value we cannot return multiple values from function.
// in call by reference we can pass large data to function.
// in call by value we cannot pass large data to function.
// in call by reference we can pass array to function.
// in call by value we cannot pass array to function.